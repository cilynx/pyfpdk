class Wall():

    ############################################################################
    # Initializer
    ############################################################################

    def __init__(self, left, right):
        self.left = left
        self.right = right

    ############################################################################
    # Length
    ############################################################################

    @property
    def length(self):
        return (((self.left[0]-self.right[0])**2)+((self.left[1]-self.right[1])**2))**0.5        
